#include "libft.h"

int		ft_nbrlen(int n)
{
	int len;

	len = 0;
	if (n == 0)
		return (1);
	(n < 0) ? (++len) : 0;
	while (n != 0)
	{
		++len;
		n /= 10;
	}
	return (len);
}
