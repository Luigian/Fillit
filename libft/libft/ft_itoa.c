#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	int		len;
	char	*str;

	i = 1;
	len = ft_nbrlen(n);
	if (!(str = ft_strnew(len)))
		return (NULL);
	if (n == -2147483648)
		return (ft_strcpy(str, "-2147483648"));
	if (n < 0)
	{
		str[0] = '-';
		n *= -1;
	}
	if (n == 0)
		str[0] = '0';
	while (n > 0)
	{
		str[len - i++] = (n % 10) + 48;
		n /= 10;
	}
	return (str);
}
